package DTO;

public class Cupom {
	private int id_cupom;
	private String cod_cupom;
	private double valor;
	
	
	
	
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public int getId_cupom() {
		return id_cupom;
	}
	public void setId_cupom(int id_cupom) {
		this.id_cupom = id_cupom;
	}
	public String getCod_cupom() {
		return cod_cupom;
	}
	public void setCod_cupom(String cod_cupom) {
		this.cod_cupom = cod_cupom;
	}
	
	
}
